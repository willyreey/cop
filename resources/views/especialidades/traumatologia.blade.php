            <p class="text-justify">La cirugía de fractura de cadera se realiza para reparar una ruptura en la parte superior del hueso del muslo. Este hueso se denomina fémur. Es parte de la articulación coxofemoral.</p>
            <p class="text-justify">Usted puede recibir anestesia general antes de esta cirugía. Esto significa que estará inconsciente y no podrá sentir dolor. También le pueden aplicar anestesia raquídea. En este tipo de anestesia, el medicamento se aplica en la espalda para anestesiarlo de la cintura para abajo. También le pueden administrar anestesia intravenosa para que usted esté dormido durante la cirugía.</p>
            <p class="text-justify">El tipo de cirugía que le practiquen dependerá del tipo de fractura que tenga.

            Si la fractura está en el cuello femoral (la parte exactamente por debajo de la cabeza del hueso), le pueden practicar una implantación de clavos en la cadera. Durante esta cirugía:

            <li> Usted se acostará sobre una mesa especial. Esto le permite al cirujano usar un equipo de rayos X para ver qué tan bien se alinean las partes del hueso de la cadera.</li>

            <li> El cirujano hará una pequeña incisión (corte) en el lado del muslo.</li>

            <li> Se colocan tornillos especiales para sostener los huesos en su posición correcta.</li>

            <li> Esta cirugía tarda de 2 a 4 horas.</li>
