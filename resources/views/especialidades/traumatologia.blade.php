        <div class="col-12 col-md-9 align-self-center">
            <p class="text-justify">La artroscopía es un procedimiento mínimamente invasivo en el cual el interior de una articulación puede visualizarse empleando una pequeña cámara que se introduce por medio de incisiones de pequeño tamaño. Esta técnica permite diagnosticar y tratar, con mayor precisión y margen de éxito, distintos problemas de rodilla sin necesidad de hospitalización y posibilitando una rápida recuperación de los pacientes. Durante el procedimiento el especialista inserta un artroscopio en la articulación, el cual es un instrumento que consta de una cámara pequeña del tamaño de un lápiz. Éste envía la imagen al monitor de una televisión, de tal forma que el cirujano puede ver las estructuras de la rodilla con gran detalle.</p>
            <p class="text-justify">La cirugía artroscópica de rodilla se realiza mayoritariamente de manera ambulatoria. El paciente entra a la sala de procedimientos y en un “abrir y cerrar de ojos” se encuentra en recuperación sin experimentar dolor o incomodidad. La anestesia para la intervención puede hacerse de modo local o por bloqueo epidural que adormezca el miembro a tratar. Luego de esto se introduce una cámara a través de una pequeña abertura en la rodilla (a cada lado del tendón rotuliano) para visualizar la parte media- interior y externa de la articulación, así como la superficie de la rótula. Por otro orificio se introducen instrumentos médicos para remover cualquier obstáculo que impida la correcta movilización de la rodilla.</p>
